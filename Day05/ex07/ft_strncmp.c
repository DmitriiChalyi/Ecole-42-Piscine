int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int k;

	k = 0;
	while (k < n)
	{
		if (s1[k] && s2[k] && s1[k] == s2[k])
			k++;
		else
			return (s1[k] - s2[k]);
	}
	return (0);
}
