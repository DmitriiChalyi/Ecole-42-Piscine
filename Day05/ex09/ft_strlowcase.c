char	*ft_strlowcase(char *str)
{
	int l;

	l = 0;
	while (str[l] != '\0')
	{
		if (str[l] >= 'A' && str[l] <= 'Z')
			str[l] = str[l] + 32;
		l++;
	}
	return (str);
}
