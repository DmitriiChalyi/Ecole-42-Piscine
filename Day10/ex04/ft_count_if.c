int		ft_count_if(char **tab, int(*f)(char*))
{
	int k;
	int i;

	k = 0;
	i = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]))
			k = k + 1;
		i = i + 1;
	}
	return (k);
}
