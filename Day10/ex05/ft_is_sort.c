int		ft_is_sort(int *tab, int length, int(*f)(int, int));
{
	int k;
	
	k = 0;
	while (k < (length - 1))
	{
		if (f(tab[k], tab[k + 1]) > 0)
			return (0);
		k = k + 1;
	}
	return (1);
}


